           function nextValidate() {


             // Validate Phone Number

             var userSuggestedPhone;
             var userSuggestedPhoneValue;
             var errorPhone = 0;


             if(document.getElementById("suggestphone")){
               userSuggestedPhone = document.getElementById("suggestphone");
               userSuggestedPhoneValue = userSuggestedPhone.value;
             }

             userSuggestedPhoneValue = userSuggestedPhone.value;
             if(userSuggestedPhoneValue<0 || userSuggestedPhoneValue>99999999999){
               userSuggestedPhone.value = 0;
               errorPhone = 1;
             }




             var userSuggestedPhoneString = userSuggestedPhoneValue.toString();

             if (userSuggestedPhoneString.length==11) {
               if(!(userSuggestedPhoneString.charAt(0)=='0' && userSuggestedPhoneString.charAt(1)=='1')){
                 errorPhone = 1;
               }
             }else if (userSuggestedPhoneString.length==10) {
               if(userSuggestedPhoneString.charAt(0)!='1'){
                 errorPhone = 1;
               }
             }


             if(userSuggestedPhoneString.length<10){
               errorPhone = 1;
             }


             if(errorPhone == 1){
               userSuggestedPhone.style.backgroundColor = "#ffdddd";
             }else{
               userSuggestedPhone.style.backgroundColor = "#ffffff";
             }




             // Validate Name of User
             var errorUsername = 0;
             var suggestNameElement = document.getElementById("suggestname");
             var suggestName = suggestNameElement.value;
             var namelength = suggestName.length;

             for(var i = 0; i< namelength; i++){
               if(!(suggestName.charAt(i)>='a' && suggestName.charAt(i)<='z') && !(suggestName.charAt(i)>='A' && suggestName.charAt(i)<='Z') && !(suggestName.charAt(i)==' ') && !(suggestName.charAt(i)=='.')){
                 errorUsername = 1;
               }
             }


             if(namelength<4 || namelength>59){
               errorUsername = 1;
             }


             if (suggestName == "") {
               errorUsername = 1;
             }


             if(errorUsername == 1){
               suggestNameElement.style.backgroundColor = "#ffdddd";
             }else{
               suggestNameElement.style.backgroundColor = "#ffffff";
             }


             if (errorUsername == 0 && errorPhone == 0) {
               first_page_of_suggest_products.style.display = "none";
               second_page_of_suggest_products.style.display = "block";
               suggeststep1.style.opacity = "0.5";
               suggeststep2.style.opacity = "1";
             }



             return false;
           }




           function previousValidate() {
             second_page_of_suggest_products.style.display = "none";
             first_page_of_suggest_products.style.display = "block";
             suggeststep2.style.opacity = "0.5";
             suggeststep1.style.opacity = "1";
             return false;
           }


           function finalSuggestionSubmit() {



             var usersuggestedproducts = document.getElementById("usersuggestedproducts");
             var usersuggestedproductsvalue = usersuggestedproducts.value;


             if(usersuggestedproductsvalue.length>4 && usersuggestedproductsvalue.length<4000){
               usersuggestedproducts.style.backgroundColor = "#ffffff";



              submitSuggestionAjax();


               return false;
             }else{
               usersuggestedproducts.style.backgroundColor = "#ffdddd";
               return false;
             }



           }



         function submitSuggestionAjax() {

           var prevSuggestion = document.getElementById("prevSuggestionBtn");
           var finalSubmitBtn = document.getElementById("finalSubmitBtn");

           document.getElementById("usersuggestedproducts").disabled = true;
           document.getElementById("usersuggestedproducts").style.opacity = "0.7";

           prevSuggestion.disabled = true;
           finalSubmitBtn.disabled = true;

           prevSuggestion.style.cursor = "progress";
           finalSubmitBtn.style.cursor = "progress";

           prevSuggestion.style.opacity = "0.7";
           finalSubmitBtn.style.opacity = "0.7";



           prevSuggestion.remove(this);
           finalSubmitBtn.innerHTML = 'Submitting <i class="fa fa-circle-o-notch fa-spin"></i>';


           var xhttp = new XMLHttpRequest();
           xhttp.onreadystatechange = function() {
             if (this.readyState == 4 && this.status == 200) {
               var requestResponse = this.responseText;
               requestResponse = requestResponse.trim();

               if(requestResponse!="N"){
                 var thank_you_text = '<div id="thank_you_suggestion_container">' +
                   '<div style="display:inline-table;">' +
                     '<img src="images/undraw_confirmation_2uy0.svg">' +
                     '<p style="text-align:left;">Hello <b>'+ requestResponse +'</b>, <br>Your valuable suggestion has been received by BosheBoshe. <br>Thank you.</p>' +
                   '</div>' +
                 '</div>';

                 inside_suggest_container.innerHTML = thank_you_text;
               }

               //





             }
           };


           xhttp.open("POST", "suggestionajax.php", true);
           xhttp.setRequestHeader("Content-type", "application/x-www-form-urlencoded");
           xhttp.send("suggestionName="+ document.getElementById("suggestname").value + "&suggestionNumber=" + document.getElementById("suggestphone").value + "&suggestionText=" + document.getElementById("usersuggestedproducts").value );

         }





