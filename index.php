          <script>

          var inside_suggest_container = document.getElementById("inside_suggest_container");




          var first_page_of_suggest_products = document.getElementById("first_page_of_suggest_products");
          var second_page_of_suggest_products = document.getElementById("second_page_of_suggest_products");

          var suggeststep1 = document.getElementById("suggeststep1");
